function addGuestToMainQueue() {
  if (queues.main.occupied[queues.main.occupied.length-1]) {
    return;
  }
  if (preshows.censuses.mainQueue>2427) {
    return;
  }
  preshows.censuses.mainQueue++;
  addHumanishCircle({
    x : queues.main.points[queues.main.points.length-1][0],
    y : queues.main.points[queues.main.points.length-1][1],
    state : "queue",
    queueData : queues.main,
    space : SPACES.MAINQUEUE
  });
}

